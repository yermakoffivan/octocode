/**
 * Generate src/lsp/serverManifestData.ts from src/lsp/serverManifest.json.
 *
 * serverManifest.json is the human-edited source of truth for the LSP
 * auto-download manifest. This script emits the TS module that the runtime
 * imports (src/lsp/serverManifest.ts -> MANIFEST), so the compiled dist needs
 * no JSON-copy step.
 *
 * Hard rule: every asset MUST have a pinned `sha256` (string). A null/missing
 * checksum would make the asset undownloadable (serverProvisioner refuses on
 * null) — emitting a manifest with null checksums hides that behind a build
 * that looks green. So this script FAILS the build on any null sha256 instead
 * of silently producing a manifest that can't download.
 *
 * Idempotent: re-running with the .json unchanged produces a byte-identical .ts.
 * Run via `yarn manifest:gen`. The `verify:manifest` gate regenerates and diffs.
 */

import { readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const srcPath = join(root, 'src', 'lsp', 'serverManifest.json')
const outPath = join(root, 'src', 'lsp', 'serverManifestData.ts')

function fail(message) {
  console.error(`manifest:gen failed: ${message}`)
  process.exit(1)
}

const manifest = JSON.parse(readFileSync(srcPath, 'utf8'))

if (typeof manifest !== 'object' || manifest === null) {
  fail('serverManifest.json is not a JSON object')
}
if (typeof manifest.version !== 'number') {
  fail('serverManifest.json missing numeric `version`')
}
if (typeof manifest.servers !== 'object' || manifest.servers === null) {
  fail('serverManifest.json missing `servers` object')
}

const comment =
  typeof manifest.$comment === 'string'
    ? manifest.$comment
    : 'Auto-download manifest for portable, toolchain-free language servers.'

// Validate: every asset has a pinned sha256 (non-empty hex string).
const nullSums = []
for (const [serverName, server] of Object.entries(manifest.servers)) {
  const platforms = server.platforms ?? {}
  for (const [platformId, asset] of Object.entries(platforms)) {
    const sum = asset.sha256
    if (typeof sum !== 'string' || !/^[0-9a-f]{64}$/.test(sum)) {
      nullSums.push(`${serverName}/${platformId}`)
    }
  }
}
if (nullSums.length > 0) {
  fail(
    `assets with missing/pinned sha256 (must be a 64-char hex string):\n  - ${nullSums.join(
      '\n  - '
    )}\nPin each sha256 in serverManifest.json before regenerating.`
  )
}

// Stable key ordering: servers + platforms sorted, so output is deterministic
// regardless of the JSON insertion order.
const serverNames = Object.keys(manifest.servers).sort()

const lines = [
  '// AUTO-GENERATED by scripts/gen-server-manifest.mjs from',
  '// src/lsp/serverManifest.json — DO NOT EDIT BY HAND.',
  '// Edit the .json then run `yarn manifest:gen` (or let the `verify:manifest`',
  '// gate regenerate it). This file is imported at runtime by serverManifest.ts.',
  `import type { ManifestFile } from './serverManifest.js';`,
  '',
  `export const MANIFEST: ManifestFile = ${JSON.stringify(
    { $comment: comment, version: manifest.version, servers: orderedServers(manifest.servers) },
    null,
    2
  )};`,
  '',
]

writeFileSync(outPath, lines.join('\n'), 'utf8')
console.log(`manifest:gen ok: wrote ${serverNames.length} server(s) → ${outPath}`)

/**
 * Rebuild the `servers` object with sorted server names and, within each server,
 * sorted platform keys, preserving every other field verbatim from the .json.
 * Returns a plain object suitable for JSON.stringify.
 */
function orderedServers(servers) {
  const out = {}
  for (const name of Object.keys(servers).sort()) {
    const server = servers[name]
    const orderedServer = {}
    for (const key of Object.keys(server).sort()) {
      if (key === 'platforms') {
        const orderedPlatforms = {}
        for (const platformId of Object.keys(server.platforms).sort()) {
          orderedPlatforms[platformId] = server.platforms[platformId]
        }
        orderedServer.platforms = orderedPlatforms
      } else {
        orderedServer[key] = server[key]
      }
    }
    out[name] = orderedServer
  }
  return out
}
